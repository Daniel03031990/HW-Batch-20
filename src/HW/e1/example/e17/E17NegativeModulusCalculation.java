package HW.e1.example.e17;

public class E17NegativeModulusCalculation {
    public static void main(String[] args) {
        // Declare the int variables and assign values
        int divident=-21;
        int divisor=4;

        int modulusresult=divident % divisor;

        System.out.println(divident + " mod " + divisor + " = " + modulusresult);
    }
}
